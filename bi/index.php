                              <h1>История клуба</h1>
<p>История Московского клуба любителей певчих и экзотических птиц неразрывно связана с историей любительского комнатного птицеводства в России.</p>

<p>Первые упоминания об организованном птицеводстве мы находим в дневниках Кружка любителей певчей и другой вольной птицы, который был образован при Императорском Русском обществе Акклиматизации Животных и Растений (обр.30 января 1857года).
6 июня 1858 года проведено первое организационное собрание секции Птицеводства указанного общества. В него первоначально входили любители содержания комнатной и домашней птицы. «Имена Рулье, Усова, Богданова, Калиновского, фон Лехнера, Маслова и других показывают, что рациональное русское птицеводство зарождалось при непосредственном участии научных сил в деле практики, в чем и был залог его расцвета в будущем» - так писал журнал о начале деятельности организации птицеводов России.
Вот что пишет Д.М.Россинский (Председатель Кружка) о задачах Секции и впоследствии Кружка в предисловии очередного дневника. «Не с недоверием и осторожным надзором, а с живым и готовым на всякую помощь интересом, должны следить члены Общества покровительства животных за работой и деятельностью мелких и бескорыстных, искренне преданных делу, любителей комнатного птицеводства, памятуя о нравственном праве людей пользоваться всеми животными, лишь не преступая предела ненужной жестокости, и, что не узниками любителей комнатного птицеводства, являются их птички, а желанными гостями, охрана которых, идущая иногда до отказа от насущных нужд своего существования, дает хозяевам любителям возможность сделать их самоотверженную работу и забаву полезной и плодотворной не только для науки, но еще более для государства, обеспечивая ему правильное направление разработки вопроса первостепенной сельскохозяйственной важности об охране и защите пернатых друзей наших».
В 1901 году любители комнатного содержания выделились в Кружок любителей певчей и другой вольной птицы. У истоков этого кружка стояли довольно известные люди, такие как орнитолог Бутурлин С.А., писатель и орнитолог Б.М.Житков, проф. Г.А.Кожевников и другие.</p>
<p>В 1899 в С.-Петербурге, в 1906 году  и в 1908 годах в Москве состоялись Всероссийские съезды орнитологов и птицеводов с участием иностранных представителей, в программе которых были проведены выставки-ярмарки всех пород и видов сельскохозяйственных и комнатных птиц. На последнем съезде были поставлены вопросы о необходимости заключения международных соглашений по вопросам охраны насекомоядных, промысловых и других птиц путем запрета ловли и охоты способами, грозящими массовым истреблением, в особенности на путях миграции. Уже в те далекие времена наши предки задумывались и беспокоились о сохранении диких видов животных.
Интересно, что Кружок оказывал попечительскую помощь Московскому в то время еще Зоосаду в сборе средств на содержание имеющихся в саду птиц, ремонт клеток, вел контроль и давал рекомендации по улучшению содержания птиц. Здесь же неоднократно Кружком проводились ярмарки и конкурсы птиц.</p><p>
Велась большая работа по поддержанию связей с аналогичными кружками в других городах, среди которых выделялся Астраханский Кружок любителей птицеводства. Члены этого кружка наряду с птицами отечественной фауны выписывали из Германии тирольских певчих канареек, в основном самцов, и устраивали небольшие разводни. В некоторых хозяйствах ежегодный выход птенцов достигал более сотни птиц. Московский Кружок оказывал помощь другим городам в организации аналогичных кружков. В С.Петербурге в 1906 году было создано Общество любителей природы с отделением по певчим и декоративным птицам, который начал выпускать популярный ежемесячный журнал «Любитель природы».
За весь период своей деятельности (есть сведения до 1910 года), Кружком проведено около 20 выставок-конкурсов певчих и экзотических птиц, в том числе конкурсы певчих канареек, конкурсы пения соловьев. Интересно добавить, что к конкурсу пения соловьев были привлечены, кроме любителей соловьиного пения, профессиональные музыканты – в том числе член Императорского Русского музыкального общества, проф. Московской консерватории Ю.С.Сахновский. Материалы по конкурсу до сих пор представляют большой интерес не только для любителей, но и для орнитологов.
К сожалению, исторические события начала прошлого века надолго прервали общественную жизнь птицеводов.</p>
<p>Лишь в 1953 году (по другим сведениям в 1957 году) при Московском городском обществе охраны природы был создан городской клуб любителей птиц. Его основу составляли в большинстве своем любители птиц отечественной фауны. С этого года в Москве начали проходить ежегодные конкурсы пения канареек и выставки птиц.
Вышедший в 1958 году закон, запрещающий отлов певчих птиц, подорвал начавшееся объединение любителей птиц. Запрет на отлов птиц отечественной фауны поставил вне закона многочисленных любителей содержания птиц отечественной фауны. Но клуб сумел выжить благодаря набирающим силу любителям певчих канареек. Год от года, в зависимости от личности председателя и его видения роли клуба, численность то возрастала до более полутысячи человек, то сокращалась до десятков.
В 1982 году клуб стал участником XVIII Всемирного орнитологического конгресса, проводившегося в Москве. Для участников конгресса клуб провел в Государственном биологическом музее им. К.А. Тимирязева выставку птиц, за что был удостоен благодарственного письма Оргкомитета конгресса.</p>
<p>В 1983 году на Выставке достижений народного хозяйства СССР была проведена выставка птиц с участием иногородних клубов и Пражского клуба разводчиков экзотических птиц.
Наибольшего расцвета клуб достиг в 80-х годах. В это время во многих городах страны уже работали десятки клубов любителей птиц. Начались контакты клубов, совместное проведение выставок, конкурсов.  Контакты выявили массу одинаковых проблем, стоящих перед клубами: отсутствие единых организующих документов, правил проведения выставок, конкурсов, документации, клеточного оборудования, кормов и т.д. Московский и Ленинградский клубы стали инициаторами объединения клубов в первую самостоятельную всероссийскую организацию, объединяющую любителей птиц.
Фактически организация была создана в 1983 году, когда по решению Московского, Ленинградского и Харьковского клубов на ВДНХ СССР в павильоне «Охраны природы» собрались представители 13 клубов страны. На совещание, фактически на свои средства, прибыли представители Камчатской области, Салехарда, Свердловска, Челябинска, Минска, Иваново и др. Предметом обсуждения стали проблемы развития клубной деятельности, их роли в охране природы.</p>
<p>В связи с тем, что большинство клубов организационно находились под эгидой местных обществ охраны природы и не имелось Всесоюзного общества охраны природы (были только республиканские) было принято решение подготовить проект положения о Всероссийском Координационном совете клубов любителей певчих и декоративных птиц. В данном положении предусматривалось членство клубов других республик на общих основаниях.
В январе 1986 года состоялось учредительная конференция представителей клубов, а 1 февраля этого же года Положение о КСК было утверждено Центральным Советом Всероссийского общества охраны природы.
В 1986 году Министерство культуры СССР разработало модель и примерный устав любительского объединения, работающего на принципах самоорганизации, самоуправления и самофинансирования.
22 октября 1986 год Всесоюзный Координационный совет клубов был преобразован во Всесоюзное объединение клубов любителей певчих и декоративных птиц. Объединение в соответствии с Уставом получило возможность образовывать хозрасчетные подразделения, вести издательскую деятельность, получило право на аренду помещений и земли и т.п. В состав учредителей Объединения вошло 24 клубов из 8 республик СССР. Надо сказать, что во всех этих начинаниях Московский клуб занимал ведущую и организующую роль.
Объединение за период своего существования провело значительную работу: проведено несколько всесоюзных совещаний представителей клубов, 6 всесоюзных чемпионатов птиц:</p>
<ul><li>Москва, ВДНХ - ноябрь1983г.</li>
<li>Харьков - октябрь1984г.</li>
<li>Сумы - октябрь 1985г.</li>
<li>Волгоград - октябрь 1986г.</li>
<li>Ереван - 1987г.</li>
<li>Минск - 1989г.</li></ul>
<p>Четырежды наши представители клубов выезжали в Чехословакию на национальную выставку птиц в г. Оломоуц. За внеконкурсное участие ВОКЛП был награжден Почетной грамотой Чешского союза разводчиков мелких животных. В 1990 году по приглашению Югославского союза экзотических птиц, наша делегация участвовала на 38 Всемирном чемпионате птиц в г. Сплит (СФРЮ).</p>
<p>В этот же период была проведена значительная работа по налаживанию отношений с дружественными союзами других стран: подписаны договора о сотрудничестве с Венгерским союзом разводчиков птиц, Югославским, Чехословацким. Налажено сотрудничество с Английским центром изучения птиц.</p>
<p>Велась обширная работа по разработке типовых документов. Было разработано и утверждено типовое положение о клубе любителей птиц, разработана и распространена документация по организации выставок и конкурсов птиц, ведению племенной и селекционной работе, проведены курсы по подготовке судей по волнистым попугайчикам, проведены всесоюзные семинары по канароводству с выработкой основных положений судейства песни русской певчей канарейки овсяночного напева, проводились всесоюзные конкурсы пения канареек, все клубы начали получать номерные кольца для ведения селекции комнатных птиц.</p>
<p>После дефолта и последующего развала СССР, клубное движение надолго прекратило свое существование.</p>
<p>Московский клуб потерял все свои средства и имущество. Лишь изредка силами отдельных членов клуба проводились демонстрационные выставки экзотических птиц. Надо отдать должное московским канароводам, конкурсы певчих канареек не прекращались ни на один год.</p>
<p>Вместе с тем в настоящее время Московский клуб, будем надеяться временно, в очередной раз разделился на Московский городской клуб любителей певчих и экзотических птиц, Московский городской клуб любителей канареек и Московский клуб «Русская канарейка». В руководство клубов пришли новые, полные идей и опыта люди. Будем надеяться, что временные разногласия останутся только временными.</p>
 
<p>Сейчас наступило время широких возможностей. Наше любительство по сравнению с западными коллегами в силу объективных причин чрезвычайно отстало от общемирового уровня. Нам предстоит не только освоить все знания западных коллег, но и найти силы выйти на мировое сообщество со своими разработками и достижениями. Думаю, давно пора прекратить искать собственный российский путь, вместо этого, объединив все свои силы, следует присоединиться к мировому сообществу.
Малаханов Е.В.</p>
																								
								
																
